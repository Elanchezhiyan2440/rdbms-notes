CREATE TABLE CITY(CCODE VARCHAR(5) PRIMARY KEY NOT NULL CHECK(CCODE LIKE 'CT%'),CNAME VARCHAR(30));

CREATE TABLE TRUCK(TRUCKCODE VARCHAR(5) PRIMARY KEY NOT NULL CHECK(TRUCKCODE LIKE 'T%'),TTYPE VARCHAR(30) CHECK(TTYPE IN ('L','H')));

CREATE TABLE DRIVER(DCODE VARCHAR(5) PRIMARY KEY NOT NULL CHECK(DCODE LIKE 'D%'),DNAME VARCHAR(30));

CREATE TABLE DRIVE_TRUCK(TRUCKCODE VARCHAR(5),DCODE VARCHAR(5) ,DOT DATE,CCODE VARCHAR(5),PRIMARY KEY(TRUCKCODE,DCODE,DOT),FOREIGN KEY(TRUCKCODE) REFERENCES TRUCK(TRUCKCODE),FOREIGN KEY(DCODE) REFERENCES DRIVER(DCODE),FOREIGN KEY(CCODE) REFERENCES CITY(CCODE));

