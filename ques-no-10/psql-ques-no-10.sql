CREATE VIEW DRIVER_TRIPS_VIEW AS
SELECT D.DCODE, D.DNAME, COUNT(DT.TRUCKCODE) AS TRIPS_TRAVELED
FROM DRIVER D
LEFT JOIN DRIVE_TRUCK DT ON D.DCODE = DT.DCODE
GROUP BY D.DCODE, D.DNAME;


--G)
CREATE OR REPLACE FUNCTION check_drive_truck_limit()
RETURNS TRIGGER AS $$
BEGIN
    IF (
        SELECT COUNT(*)
        FROM DRIVE_TRUCK
        WHERE CCODE = NEW.CCODE AND DOT = NEW.DOT
    ) >= 2 THEN
        RAISE EXCEPTION 'Cannot insert more than two records for the same city on the same day';
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER limit_drive_truck_insert
BEFORE INSERT ON DRIVE_TRUCK
FOR EACH ROW
EXECUTE FUNCTION check_drive_truck_limit();

--H)
CREATE OR REPLACE FUNCTION get_driver_trip_count(
    IN p_driver_code VARCHAR(5),
    OUT p_trip_count INTEGER
)
LANGUAGE plpgsql
AS $$
BEGIN
    -- Initialize the trip count to 0
    p_trip_count := 0;

    -- Check if the driver code exists
    IF NOT EXISTS (SELECT 1 FROM DRIVER WHERE DCODE = p_driver_code) THEN
        RAISE EXCEPTION 'No such driver with code %', p_driver_code;
    END IF;

    -- Retrieve the total number of trips for the given driver code
    SELECT COUNT(*) INTO p_trip_count
    FROM DRIVE_TRUCK
    WHERE DCODE = p_driver_code;
END;
$$;


